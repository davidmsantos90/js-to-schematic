// Array swap example - demonstrating all array features
let arr = [5, 10];

// Read using dynamic index
let i = 0;
let a = arr[i]; // a = 5

i = 1;
let b = arr[i]; // b = 10

// Swap using array operations
let temp = arr[0]; // temp = 5
arr[0] = arr[1]; // arr[0] = 10
arr[1] = temp; // arr[1] = 5

// Verify swap worked
let x = arr[0]; // x = 10
let y = arr[1]; // y = 5
