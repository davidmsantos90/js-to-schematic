// Basic array test
let arr = [10, 20, 30];

// Static index access
let x = arr[0]; // x = 10
let y = arr[1]; // y = 20
let z = arr[2]; // z = 30

// Static index assignment
arr[0] = 100;
arr[1] = 200;
arr[2] = 300;

// Dynamic index access
let i = 1;
let val = arr[i]; // val = 200

// Dynamic index assignment
i = 0;
arr[i] = 999; // arr[0] = 999
